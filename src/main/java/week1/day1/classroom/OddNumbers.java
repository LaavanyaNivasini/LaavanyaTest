package week1.day1.classroom;

public class OddNumbers {

	public static void main(String[] args) {
		int num=20;
		System.out.println("Printing only the odd numbers from 1 to 20");
		for (int i = 1; i <= num; i++) {
			if (i % 2 != 0)
				System.out.println(i);
			
		
		}
				

	}

}
